

function [setOut]=CoordinateTran(setIn)
%*********************************************************************
%*********************************************************************
%*********************************************************************
%函数功能：坐标变换，将极坐标系数据转化为笛卡尔坐标系数据
%输入：包含Num个极坐标点的点集setIn
%输出：包含Num个笛卡尔坐标的点集setOut
%setIn=  |range|         setOut=| x  |
%        |theta|                | y  |             
%       矩阵setOut数据基于极坐标系
%       矩阵setIn数据基于笛卡尔坐标系
%作者：Shaofeng Wu 
%时间：2017.11.28
%*********************************************************************
%*********************************************************************
%*********************************************************************
%*********************************************************************
%*********************************************************************
range=setIn(1,:);
theta=setIn(2,:);
%*********************************************************************
%*********************************************************************
%Step1：将数据点由极坐标系转化为笛卡尔坐标系
alpha=theta*pi/180;   %角度制转为弧度制

%*********************************************************************
%*********************************************************************
%Step2：坐标转换计算
x=zeros(size(theta)); %设置一个大小和Theta一样的向量
y=zeros(size(theta)); %设置一个大小和Theta一样的向量

for m = 1:size(theta,2)
    x(m)=range(m)*cos(alpha(m));
    y(m)=range(m)*sin(alpha(m));
end

setOut=[x;y];









